class Solution {
    public static ArrayList<Integer> solveQueries(int n, int num, int[] a, int[][] q) {
        ArrayList<Integer> result = new ArrayList<>();
        int []freq = new int[n];
        
        for(int i=0;i<n;i++){
            int count = 1;
            for(int j=i+1;j<n;j++){
                if(a[i]==a[j]) count++;
            }
            freq[i] = count;
        }
        // System.out.println(Arrays.toString(freq));
        
        for(int i=0;i<num;i++){
            int l = q[i][0];
            int r = q[i][1];
            int k = q[i][2];
            
            int count = 0;
            for(int j=l;j<=r;j++){
                if(freq[j]==k) count++;
            }
            result.add(count);
        }
        
        return result;
    }
}
        
